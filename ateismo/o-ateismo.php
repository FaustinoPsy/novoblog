<h2 style="margin: 1em 0px 0.25em; padding: 0px; overflow: hidden; border-bottom: 1px solid #a2a9b1; font-weight: normal; font-family: &quot;Linux Libertine&quot;, Georgia, Times, serif; line-height: 1.3;"><span id="Composition" class="mw-headline">Ateísmo não é...</span></h2>
<hr>
<p>Provavelmente alguns leitores já devem ter ouvido expressões do tipo: “o ateísmo também é uma religião”, “os ateus também acreditam em algo, como a inexistência de Deus”, “é uma crença como a religião”. Embora quem diga isso às vezes pretenda ser inteligente, na verdade está sendo o oposto porque parte de uma confusão conceitual que não explica nada.<span id="more-2545"> </span></p>
<p>Quando trabalhamos com conceitos, o primeiro cuidado que precisamos ter é o de aplicá-lo corretamente, ou será usado apenas como rótulo, às vezes como insulto. Podemos ver vários exemplos disso em diferentes meios: fundamentalista, fascista, comunista são, por exemplo, conceitos que têm sido usados com frequência fora de contexto como rotulações e insultos. Quando alguém diz que o ateísmo também é uma religião não está propriamente insultando ateus, mas usando conceitos diferentes de forma equivocada.</p>
<p>Religião vem do latim re-ligare, que significa “religar”, e está relacionada, em alguns autores, aos primeiros registros de ritos fúnebres desenvolvidos pelo homem, na tentativa de não se perder para sempre de seus mortos, de se religar a eles.</p>
<p>Durkheim observa que apenas a crença em entidades sobrenaturais ou em Deus(es) não é suficiente para definir uma religião, porque esses atributos não são encontrados em todos os credos religiosos. Em sua abordagem, se há algo em comum que define uma religião é a divisão que  ela faz do mundo em dois domínios: o sagrado e o profano. São duas categorias que se opõem, portanto.</p>
<p>O sagrado se manifesta num conjunto de ritos e crenças que determinam e prescrevem comportamentos e afirmam a superioridade da coletividade sobre o indivíduo. O sagrado evoca a autoridade sacerdotal, que personifica a ordem representada pelos deuses e/ou pelo desejo ontológico de alcançar um estado elevado de existência e consciência. O profano, como abordou Mircea Eliade, representa o caos, o desconhecido, o espaço não consagrado, o não-ser.</p>
<p id="inline-ad-0"> </p>
<p>A dicotomia sagrado/profano pode ser encontrada com características distintas, portanto, em todos os sistemas religiosos e é uma particularidade deles. São esses sentidos que formam o cerne do pensamento e da ação do homem religioso. A noção de sagrado também comporta gestos de reverência e adoração, seja para com forças da natureza tomadas como divindades, seja para um Deus considerado criador da natureza, ou mesmo para com uma liderança religiosa do passado.</p>
<p>O ateísmo, por outro lado, não comporta nada que se assemelhe a uma noção de sagrado. Ao contrário, o ateísmo é uma postura intelectual que reflete a dessacralização da consciência. O ateu não age eticamente porque tem medo da punição divina, mas pelo senso de responsabilidade para com o outro. A essência do ateísmo é o humanismo.</p>
<p>O ateísmo também é refratário à submissão a qualquer autoridade. Isso torna difícil sua união em torno de ideais comuns. Ateus podem ter em comum a descrença, mas podem divergir amplamente em torno de outros assuntos como política, economia, entre outros. Não formam um bloco homogêneo e podem divergir até mesmo sobre conceitos e referenciais que adotam em matéria de assuntos religiosos.</p>
<p>Pode-se objetar que as religiões também não são homogêneas. De uma mesma Bíblia se criam inúmeras seitas e segmentos religiosos. Mas todas elas reconhecem uma Bíblia e determinada interpretação dela como autoridade final. Todas elas reconhecem a autoridade de um ou mais sacerdotes, que pode o fundador e líder ou alguém que alega ser herdeiro dele. Todas elas possuem uma distinção clara entre sagrado e profano. Nada disso tem qualquer relação com o ateísmo.</p>
<p>A própria noção de fundamentalismo não pode ser usada para ateus. O conceito de fundamentalismo não tem sentido fora de um contexto religioso monoteísta. É necessária a crença em um livro sagrado e em sua literalidade para que fale de fundamentalismo. O próprio termo, em sua origem, está associado ao protestantismo norte-americano do início do século XX, que reafirmou a literalidade das narrativas bíblicas rejeitadas pela teologia liberal do século XIX e pela evolução darwinista, expressas num conjunto de documentos intitulados The Fundamentals (daí o uso da expressão).</p>
<p>Você pode não gostar do ateísmo nem de autores ateus, mas não é prudente chamar as coisas pelos nomes errados. Ateísmo não é religião.</p>
<p><strong>Bertone Sousa</strong>, professor, historiador e AUTOR ORIGNAL DO TEXTO ACIMA</p>

