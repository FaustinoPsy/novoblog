  <h3>Criticas</h3>
  <p>O budismo tem, como fundamento de sua doutrina, a extinção do desejo. Porém muitos pensadores renomados defendem que tal objetivo é simplesmente impossível e mesmo não desejável, dada a natureza do homem. O filósofo inglês Thomas Hobbes (1588-1679), por exemplo, relacionava a felicidade ao desejo, o qual nunca seria totalmente satisfeito, pois sempre mudaria de um objeto para outro [1]. No mesmo sentido, o filósofo neerlandês Espinoza (1632-1677) afirmou que o desejo é a essência do homem e que negá-lo seria negar a própria condição humana[2]. O filme estadunidense de 2004 "Antes do pôr do sol" (Before sunset), dirigido por Richard Linklater, faz a personagem Celine criticar o budismo, ao afirmar que a falta de desejo preconizada pelo budismo é um sintoma de depressão.</p>
