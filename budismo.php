  <h3>Origem</h3>
  <p>O budismo é uma religião indiana baseada nos ensinamentos de Sidarta Gautama, conhecido como o Buda. De caráter filosófico e não teísta, surgiu na Índia Antiga como uma tradição ascética entre os séculos VI e IV a.C. Atualmente é a quarta maior religião do mundo, com mais de 520 milhões de seguidores (cerca de 7% a 8% da população global),[4] conhecidos como budistas. No Brasil, segundo o censo de 2010, residem aproximadamente 245 mil budistas.[5][6] Em Portugal, há cerca de 64 mil budistas. O budismo abrange diversas tradições, crenças e práticas espirituais baseadas nos ensinamentos do Buda e em suas interpretações. Os dois maiores ramos do budismo são o theravāda ("Escola dos Anciões" em páli) e o mahāyāna ("O Grande Veículo" em sânscrito).

Como expresso nas Quatro Nobres Verdades do Buda, a meta do budismo é a superação do sofrimento (dukkha) causado pelo desejo e pela ignorância em relação à verdadeira natureza da realidade, formada pela impermanência e pela inexistência da alma dos seres vivos. A maioria das tradições budistas se concentram na superação do eu individual através da conquista do nirvana ou da busca do caminho de Buda, o que leva ao fim do ciclo de morte e renascimento.
</p><p>
De acordo com a narrativa convencional, o Buda nasceu em Lumbini (hoje, patrimônio mundial da Organização das Nações Unidas para a Educação, a Ciência e a Cultura) por volta do ano 566 a.C. e cresceu em Capilvasto: ambos, atuais localidades nepalesas. Logo após o nascimento de Sidarta, um astrólogo visitou o pai do jovem príncipe, Suddhodana, e profetizou que Sidarta ou iria se tornar um grande rei, ou renunciaria ao mundo material para se tornar um homem santo se, porventura, visse a vida fora das paredes do palácio.

O rei Suddhodana estava determinado a ver o seu filho se tornar um rei e, assim, impediu que ele saísse do palácio. Mas, aos 29 anos, apesar dos esforços de seu pai, Sidarta se aventurou por além do palácio diversas vezes. Em uma série de encontros (em locais conhecidos pela cultura budista como "quatro pontos"), ele soube do sofrimento das pessoas comuns, encontrando um homem velho, um outro doente, um cadáver e, finalmente, um asceta sadhu, representando a busca espiritual. Essas experiências levaram Gautama, finalmente, a abandonar a vida material e ir em busca de uma vida espiritual.

Sidarta Gautama estudou sob diferentes mestres e desencantou-se com o resultado alcançado pelo que ensinavam. Chegou a praticar ascese rígida, como jejum prolongado, restrição da respiração, e outras formas de exposição a dor, muito comuns naquele tempo na Índia, e quase morreu ao longo do processo. Mas houve um episódio no qual uma jovem lhe ofereceu comida e ele aceitouː isso marcou sua renúncia a tais práticas. Concluiu que as práticas ascéticas extremas não traziam os resultados que buscava. Deduziu, então, que as práticas eram prejudiciais aos praticantes. Ele abandonou o ascetismo, concentrando-se na meditação anapanasati, através da qual descobriu o que hoje os budistas chamam de "caminho do meio": um caminho que não passa pela luxúria e pelos prazeres sensuais, mas que também não passa pelas práticas de mortificação do corpo. Em outras palavras, o caminho do meio não seria o caminho do apego a qualquer coisa, nem também o caminho da negação ou aversão a qualquer coisa, e sim uma terceira via.
</p>
