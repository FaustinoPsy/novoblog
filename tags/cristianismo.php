<div id="Criticas" class="tabcontent">
  <h3>Criticas</h3>
  <p>As críticas ao cristianismo e aos cristãos remontam à Era Apostólica, com o Novo Testamento registrando atritos entre os seguidores de Jesus e os fariseus e escribas (por exemplo, Mateus 15: 1-20 e Marcos 7: 1-23). No século II, o cristianismo foi criticado pelos judeus por vários motivos, por ex. que as profecias da Bíblia Hebraica não poderiam ter sido cumpridas por Jesus, visto que ele não teve uma vida de sucesso. Além disso, um sacrifício para remover pecados antecipadamente, para todos ou como um ser humano, não se encaixava no ritual de sacrifício judaico; além disso, diz-se que Deus julga as pessoas por seus atos e não por suas crenças. Um dos primeiros ataques abrangentes ao cristianismo veio do filósofo grego Celso, que escreveu A Verdadeira Palavra, uma polêmica criticando os cristãos como membros não lucrativos da sociedade. Em resposta, o padre gegro Orígenes publicou seu tratado Contra Celsum, uma obra seminal da apologética cristã, que sistematicamente abordou as críticas de Celso e ajudou a trazer ao cristianismo um nível de respeitabilidade acadêmica.

No século III, as críticas ao cristianismo aumentaram. Rumores selvagens sobre os cristãos foram amplamente divulgados, alegando que eles eram ateus e que, como parte de seus rituais, devoravam crianças e se engajavam em orgias incestuosas. O filósofo neoplatonista Porfírio escreveu o Adversus Christianos, de quinze volumes, como um ataque abrangente ao cristianismo, em parte com base nos ensinamentos de Plotino.

Por volta do século XII, a Mishné Torá (por Maimônides) estava criticando o cristianismo com base na adoração de ídolos, em que os cristãos atribuíam divindade a Jesus, que tinha um corpo físico. No século XIX, Nietzsche começou a escrever uma série de polêmicas sobre os ensinamentos "não naturais" do cristianismo (por exemplo, abstinência sexual), e continuou sua crítica ao cristianismo até o fim de sua vida. No século XX, o filósofo Bertrand Russell expressou sua crítica ao cristianismo em Why I Am Not a Christian, formulando sua rejeição do cristianismo no cenário de argumentos lógicos.

A crítica ao cristianismo continua até hoje, por exemplo, teólogos judeus e muçulmanos criticam a doutrina da Trindade sustentada pela maioria dos cristãos, afirmando que esta doutrina em vigor assume que existem três deuses, indo contra o princípio básico do monoteísmo. O erudito do Novo Testamento, Robert M. Price, esboçou a possibilidade de que algumas histórias da Bíblia sejam baseadas parcialmente em mitos.</p>
