  <h3>Variações</h3>
  <p>Desde o princiio do cristianismo a divisão foi sempre um marco central dessa religião.</p>
  <p><b>Concilio de jerusalém</b></p>
  <p>A primeira divisão se deu pela questão da circuncisão e costumes alimentares, houve o concilio de jerusalém, onde foi decidido que Paulo iria aos gentios e Pedro, Tiago e João continuaria pregando aos judeus.</p>
    <p><b>Oriente contra Ocidente</b></p>
  <p>Houve a grade divisão da Igreja Oriental e Ocidental, por conta da questão do Bispo de roma da igreja ocidental ter maior autoridade que os do Oriente, essa foi uma grande roptura no cristianismo, mas nada como a roptura da reforma protestante.</p>
    <p><b>Reforma Protestante</b></p>
  <p>Reforma (alternativamente chamada de Reforma Protestante ou Reforma Europeia) foi um movimento importante dentro do cristianismo ocidental na Europa do século XVI que representou um desafio religioso e político para a Igreja Católica e em particular para a autoridade papal, decorrente do que eram percebidos como erros, abusos e discrepâncias cometidos pela Igreja. A Reforma foi o início do protestantismo, além de ser considerada um dos eventos históricos que marcam o fim da Idade Média e o início do período moderno na Europa.</p>
  <p><b>Denominações contra Denominações</b></p>
  <p>Hoje dentro do protestantismo há muitas denominações e cada uma delas se percebe como a única verdadeira, algumas acusam veemente outras de serem hereges ou prostitutas que levam seus membros para o inferno, cada uma divergem da outra por questões dogmaticas ou doutrinarias, algumas por conta de usos e costumes, e outras por interpretações erroneas da bíblia.</p>
