<p>
 <p>O Espiritismo é uma doutrina de cunho científico-filosófico, estabelecida no século XIX, por Denizard Hippolyte Leon Rivail, mais conhecido como Allan Kardec. Diferentemente do que se imagina, Kardec não foi algum tipo de místico ou excêntrico que sempre se dedicou ao mundo imaterial. Grande parte de sua vida foi dedicada às aulas ministradas na escola gerenciada por Johann Pestalozzi. Suas primeiras experimentações com o mundo dos espíritos aconteceram durante seus estudos envolvendo o magnetismo.</p>
      <!-- <p><img src="images/adv1.gif" width="593" height="530"></p> -->
      <p>Foi nesse período em que realizou as sessões de “mesas girantes”, onde acontecia a movimentação de objetos sem algum tipo de intervenção perceptível. O interesse por esses fenômenos aprofundou seu interesse pelo mundo dos desencarnados, dando origem à obra “O Livro dos Espíritos”. O livro, que hoje fundamenta os ensinamentos do espiritismo, foi o primeiro passo de uma vida inteiramente dedicada ao conhecimento e à explicação dos fenômenos espíritas.</p>
      <p >Interessado em divulgar seus conhecimentos, Kardec cria a “Revue Spirite” (Revista Espírita) e funda a Sociedade Parisiense de Estudos Espirituais. A via impressa e a fundação foram meios eficazes para que a nova crença religiosa se difundisse pela França, ganhando diversos adeptos. Uma parte significativa desses seguidores fazia parte das classes trabalhadoras que viviam a efervescência da exploração da força de trabalho capitalista e os impactantes ideais do socialismo.</p>
      
      <p>Fundando diversos dos pontos da prática espírita, Allan Kardec foi fortemente influenciado pelo cientificismo e o pensamento evolucionista. De fato, sua ação doutrinária estabelece no espírita uma relação na qual o homem pode e deve compreender, por meio da razão e do diálogo, os fenômenos que outras religiões assumem de maneira misteriosa e resignada. Por isso que os espíritas são bastante conhecidos como consumidores assíduos da literatura espírita.</p>
	  
	  <p>Estes livros geralmente relatam a experiência de algumas pessoas com o mundo dos espíritos, legitimando a importância do conhecimento deste outro mundo. Uma vertente da literatura espírita é voltada para a reprodução do conhecimento repassado por espíritos. Esta atividade é mais conhecida como psicografia e atrai a curiosidade de pessoas dentro e fora da religião espírita. Entre outros autores, o brasileiro Chico Xavier tem grande destaque na produção deste tipo de literatura.</p>
<p>
Outra característica marcante do espiritismo envolve a realização dos passes em que um membro dotado de maior sensitividade procura harmonizar o estado psíquico e emocional de outros praticantes. Este tipo de procedimento também possuiu outro desdobramento no qual alguns espíritas conseguem intervir sobre alguns males físicos por meio da cura espiritual.
</p><p>
Além de empreender esse tipo de ação religiosa, o espiritismo também tem uma prática social arraigada entre seus membros. Segundo os princípios espíritas, o indivíduo deve praticar a caridade como uma forma de estabelecer a melhora de sua condição espiritual presente e de suas posteriores reencarnações. Este princípio se baseia na ideia de que a humanidade faz parte de um estágio de evolução espiritual.
</p><p>
Este princípio se sustenta na premissa de que o mundo terreno integra outra infinidade de mundos onde os espíritos habitam graus de evolução superior e inferior. Esse mesmo princípio evolucionista também fundamenta a explicação para o sofrimento humano, sendo este compreendido como resultado da intervenção de espíritos ou das ações ruins praticadas em outras reencarnações.
</p><p>
No Brasil, o espiritismo encontrou um grande espaço de divulgação ocupando o atual posto de maior nação espírita do mundo. Diferindo-se das demais religiões que praticam este contato direto com os espíritos, o espiritismo é comumente chamado de “kardecismo”, estabelecendo apenas um elemento distintivo em relação a outras crenças como a umbanda, o candomblé e a santeria.
</p><p>
Abrindo portas para a doutrina cristã, o espiritismo tem um elo relativo com o texto bíblico e os evangelhos. Sem ocupar a posição fundamental da obra de Kardec, a Bíblia é utilizada como uma das várias referências de compreensão do mundo espiritual. Concomitantemente, a vida de Jesus Cristo é considerada um modelo de evolução espiritual também obtido na história de vida de outros indivíduos iluminados.
</p><p>
Combinando fé, razão e caridade, o espiritismo pautou uma nova experiência religiosa marcada pela experiência e a investigação. Conforme definido por Luiz Gonzaga de Souza “espiritismo é amor, é felicidade, é abnegação, é labuta pelo bem, é autoconsciência das verdades absolutas, é caridade e, é, sobretudo, igualdade, com fraternidade e liberdade entre os povos”.</p><p>

