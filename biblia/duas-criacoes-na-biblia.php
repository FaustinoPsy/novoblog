<h2 >As duas criações </h2>
<div class="row"> 
<div class="span8 tabcontentblog">
<p>
A narrativa da criação do Gênesis é o mito da criação tanto do Judaísmo quanto do Cristianismo . A narrativa é composta de duas histórias, aproximadamente equivalente aos dois primeiros capítulos do livro do Gênesis . No primeiro, Elohim (a palavra hebraica genérica para Deus ) cria os céus e a Terra em seis dias, depois repousa, abençoa e santifica o sétimo (isto é, o sábado bíblico ). Na segunda história, Deus, agora referido pelo nome pessoal de Yahweh , cria Adão , o primeiro homem, do pó e o coloca noJardim do Éden , onde ele recebe domínio sobre os animais. Eva , a primeira mulher, é criada a partir de Adão e como sua companheira.

Tomando emprestados temas da mitologia mesopotâmica , mas adaptando-os à crença do povo israelita em um Deus , o primeiro grande esboço abrangente do Pentateuco (a série de cinco livros que começa com Gênesis e termina com Deuteronômio ) foi composto no final do século 7 ou o 6º século AEC (a fonte jahwista ) e foi posteriormente expandido por outros autores (a fonte sacerdotal ) em uma obra muito parecida com o Gênesis como é conhecido hoje. As duas fontes podem ser identificadas na narrativa da criação: Sacerdotal e Jahwística. A narrativa combinada é uma crítica doTeologia mesopotâmica da criação: Gênesis afirma o monoteísmo e nega o politeísmo . Robert Alter descreveu a narrativa combinada como "atraente em seu caráter arquetípico, sua adaptação do mito para fins monoteístas".

Nos últimos séculos, alguns crentes usaram essa narrativa como evidência de criacionismo literal , levando-os a negar a evolução subsequentemente .</p>
<h2 >Composição </h2>
<p>
Embora a tradição atribua Gênesis a Moisés , os estudiosos bíblicos sustentam que, junto com os quatro livros seguintes (constituindo o que os judeus chamam de Torá e os estudiosos da Bíblia chamam de Pentateuco), é "uma obra composta, produto de muitas mãos e períodos". Uma hipótese comum entre os estudiosos da Bíblia hoje é que o primeiro grande esboço abrangente do Pentateuco foi composto no final do século 7 ou 6 aC (a fonte jahwista ), e que isso foi posteriormente expandido pela adição de várias narrativas e leis (a fonte sacerdotal ) em uma obra muito parecida com a que existe hoje.

Quanto ao pano de fundo histórico que levou à criação da própria narrativa, uma teoria que ganhou considerável interesse, embora ainda controversa, é a "autorização imperial persa". Isso propõe que os persas , após sua conquista da Babilônia em 538 AEC, concordaram em conceder a Jerusalém uma grande medida de autonomia local dentro do império, mas exigiram que as autoridades locais produzissem um único código de leiaceito por toda a comunidade. Além disso, propõe que havia dois grupos poderosos na comunidade - as famílias sacerdotais que controlavam o Templo e as famílias de proprietários de terras que constituíam os "anciãos" - e que esses dois grupos estavam em conflito por muitas questões, e que cada um tinha seus própria "história das origens", mas a promessa persa de maior autonomia local para todos forneceu um poderoso incentivo para cooperar na produção de um único texto.
</p>
<h2 >Estrutura </h2>
<p>
A narrativa da criação é composta de duas histórias, aproximadamente equivalente aos dois primeiros capítulos do livro de Gênesis (não há divisões de capítulos no texto hebraico original, veja capítulos e versículos da Bíblia ). A primeira conta ( Gênesis 1: 1-2: 3 ) emprega uma estrutura repetitiva de fiat divino e realização, em seguida, a declaração "E foi a tarde ea manhã, o [ ] dia", para cada um dos seis dias da criação. Em cada um dos primeiros três dias, há um ato de divisão: o primeiro dia divide a escuridão da luz, o segundo dia as "águas acima" das "águas abaixo" e o terceiro dia o mar da terra. Em cada um dos próximos três dias, essas divisões são povoadas: o dia quatro povoa a escuridão e a luz com o Sol, a Lua e as estrelas; o dia cinco povoa mares e céus com peixes e aves; e finalmente as criaturas terrestres e a humanidade povoam a terra.

A consistência evidentemente não era vista como essencial para contar histórias na literatura do antigo Oriente Próximo. As histórias sobrepostas de Gênesis 1 e 2 são contraditórias, mas também complementares, com a primeira (a história sacerdotal) preocupada com a criação de todo o cosmos, enquanto a segunda (a história Yahwista) se concentra no homem como agente moral e cultivador de seu ambiente. A narrativa de sete dias altamente regulamentada de Gênesis 1 apresenta um Deus onipotente que cria uma humanidade semelhante a um deus, enquanto a criação de um dia de Gênesis 2 usa uma narrativa linear simples, um Deus que tanto pode falhar quanto ter sucesso, e uma humanidade que não é divina, mas é punida por atos que os levariam a se tornar semelhante a um deus. Até a ordem e o método de criação são diferentes. "Juntos, esta combinação de caráter paralelo e perfil contrastante apontam para a origem diferente dos materiais em Gênesis 1 e Gênesis 2, por mais elegantes que tenham sido agora combinados."

Os relatos principais em cada capítulo são unidos por uma ponte literária em Gênesis 2: 4 , "Estas são as gerações dos céus e da terra quando foram criadas." Isso ecoa a primeira linha de Gênesis 1, "No princípio Deus criou o céu e a terra", e é revertida na frase seguinte, "... no dia em que o Senhor Deus fez a terra e os céus". Este verso é uma das dez "gerações" ( em hebraico : תולדות Toledot ) frases usadas em todo Genesis, que fornecem uma estrutura literária do livro. Eles normalmente funcionam como títulos para o que vem depois, mas a posição deste, o primeiro da série, tem sido o assunto de muito debate.
</p>
<h2 >Influência mesopotâmica </h2>
<p>
Gênesis 1 apresenta diferenças marcantes e semelhanças marcantes com o mito da criação nacional da Babilônia , o Enuma Elish . Do lado das semelhanças, ambos começam de um estágio de águas caóticas antes que qualquer coisa seja criada, ambos em um "firmamento" em forma de cúpula fixa separa essas águas da Terra habitável, e ambos concluem com a criação de um humano chamado "homem" e a construção de um templo para o deus (em Gênesis 1, esse templo é todo o cosmos). Do lado dos contrastes, Gênesis 1 é monoteísta, não faz nenhuma tentativa de explicar as origens de Deus, e não há nenhum traço de resistência à redução do caos à ordem (grego: teomaquia , lit. "luta contra Deus"), todos os quais marcam os relatos da criação na Mesopotâmia. Ainda assim, Gênesis 1 tem semelhanças com o Ciclo Baal do vizinho de Israel, Ugarit . A mitologia comparada fornece perspectivas históricas e interculturais para a mitologia judaica . Ambas as fontes por trás da narrativa da criação do Gênesis emprestaram temas da mitologia mesopotâmica , mas os adaptaram à sua crença em um Deus , estabelecendo uma criação monoteísta em oposição ao mito da criação politeísta dos vizinhos do antigo Israel .

O Enuma Elish também deixou traços em Gênesis 2. Ambos começam com uma série de declarações do que não existia no momento em que a criação começou; o Enuma Elish tem uma nascente (no mar) como o ponto onde a criação começa, paralela à nascente (na terra - Gênesis 2 é notável por ser uma história de criação "seca") em Gênesis 2: 6 que "regou toda a face do solo "; em ambos os mitos, Yahweh / os deuses primeiro criam um homem para servi-los, depois os animais e a vegetação. Ao mesmo tempo, e como em Gênesis 1, a versão judaica mudou drasticamente seu modelo babilônico: Eva, por exemplo, parece cumprir o papel de uma deusa-mãe quando, em Gênesis 4: 1, ela diz que "criou um homem com Yahweh", mas ela não é um ser divino como sua contraparte babilônica.

Gênesis 2 tem paralelos próximos com um segundo mito mesopotâmico, o épico de Atra-Hasis - paralelos que de fato se estendem por Gênesis 2–11 , da Criação ao Dilúvio e suas consequências. Os dois compartilham vários detalhes do enredo (por exemplo, o jardim divino e o papel do primeiro homem no jardim, a criação do homem a partir de uma mistura de terra e substância divina, a chance de imortalidade, etc.), e têm um semelhante tema geral: o esclarecimento gradual da relação do homem com Deus (s) e os animais.
</p>
<h2 >Criação pela palavra e pela criação de combate </h2>
<p>
As narrativas em Gênesis 1 e 2 não eram os únicos mitos da criação no antigo Israel, e a evidência bíblica completa sugere dois modelos contrastantes. O primeiro é o modelo " logos " (que significa fala), onde um Deus supremo "fala" à existência de matéria adormecida. O segundo é o modelo " agon " (que significa luta ou combate), no qual é a vitória de Deus na batalha contra os monstros do mar que marcam sua soberania e poder. Gênesis 1 é um exemplo de criação pela palavra, enquanto Salmo 74 e Isaías 51são exemplos da mitologia "agon", que remete a um mito cananeu em que Deus cria o mundo vencendo as divindades da água: "Desperta, desperta! ... Foi você que cortou Raabe em pedaços, que perfurou o Dragão! Foi você que secou o Mar, as águas do grande Abismo, que fez dos abismos do Mar uma estrada pela qual os remidos pudessem andar ... ”

</p>
