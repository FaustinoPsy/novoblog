<strong><p>Pós-milenarismo</strong></p>
<img src="img/premilenio2.jpg" style="width:80%" alt="" />
<strong><p>Pos-milenio</strong></p>
Creem que através da evangelização, o mundo finalmente será de Cristo. Cristo voltará a terra no fim do milênio. Surgiu com as grandes missões do século XVIII e XIX, porém com as Grandes Guerras Mundiais e a percepção de que a humanidade não progrediu com a mensagem do Evangelho diminuiu o número de adeptos desta vertente escatológica.
<strong><p>Amilenarismo</strong></p>
<img src="img/amilenarismo.jpg" style="width:80%" alt="" />
<strong><p>Amilenismo</strong></p>
Aqui o milênio é simbólico, e o tempo se refere à primeira e à segunda vinda de Cristo, nada aqui é literal exceto alguns fatos históricos. É a escola mais antiga que surgiu, tendo existido desde os primórdios da igreja cristã.</p>
