<h2 style="margin: 1em 0px 0.25em; padding: 0px; overflow: hidden; border-bottom: 1px solid #a2a9b1; font-weight: normal; font-family: &quot;Linux Libertine&quot;, Georgia, Times, serif; line-height: 1.3;"><span id="Composition" class="mw-headline">Escatologia </span></h2>
<p>Escatologia (do grego antigo εσχατος, "último", mais o sufixo -logia) é uma parte da teologia e filosofia que trata dos últimos eventos na história do mundo ou do destino final do gênero humano, comumente denominado como fim do mundo. Em muitas religiões, o fim do mundo é um evento futuro profetizado no texto sagrado.
<strong><p>Escolas de interpretação</strong></p>
Existem pelo menos três grandes correntes de interpretação das profecias bíblicas escatológicas dentro do cristianismo:
<strong><p>Preterismo</strong></p>: as profecias se cumpriram no passado, tendo pouca ou nenhuma relevância na vida dos seres humanos;
<strong><p>Futurismo</strong></p>: o cumprimento se dará num futuro distante, sem que as pessoas saibam quando, como e o que realmente acontecerá.
<strong><p>Historicismo</strong></p>: os eventos proféticos, descritos literal ou simbolicamente, ocorrem com o passar do tempo e são históricos, podendo ser interpretados de expressões-chave contidas na profecia.


<strong><p>Historico</strong></p>
130 d.C. Justino, o Mártir acreditava que Deus estaria a atrasar o fim do mundo porque desejava que o Cristianismo se tornasse uma religião mundial. Por volta do Século III a maioria dos professos cristãos acreditava que o fim dos tempos ocorreria depois de suas mortes. Em 250 d.C. Cipriano, Bispo de Cartago, escreveu que os pecados dos cristãos eram um prelúdio e prova de que o fim dos tempos estava próximo. Alguns, recorrendo às Tradições Judaicas, fixaram o fim das eras na Sexta Idade do Mundo. Usando este sistema, o fim foi anunciado para 202 d.C. mas, quando esta data passou, foi fixada uma nova data. Na época de Clóvis I, considerado o fundador da França e que se converteu ao catolicismo após ser entronizado como rei em 481 d.C., alguns escritores católicos haviam apresentado a ideia de que o ano 500 d.C marcaria o fim do mundo. Depois de 500 d.C., a importância e a expectativa da vinda do fim do mundo ou das eras como parte dos fundamentos do Cristianismo foi marginalizada e gradualmente abandonada. Apesar disso, surgiu um temporário reavivamento dos temores relacionados com o fim dos tempos com a aproximação do milésimo ano do nascimento de Cristo. Muitos acreditavam na iminência do fim do mundo ao se aproximar o ano 1000. Segundo consta, as atividades artísticas e culturais nos mosteiros da Europa praticamente cessaram. Eric Russell observou no seu livro "Astrology and Prediction": "'Em vista da proximidade do fim do mundo’ era uma expressão muito comum nos testamentos validados durante a segunda metade do Século X."
<strong><p>Variações</strong></p>
A doutrina do cristianismo sofreu variações pelo tempo. Desde os tempos do Antigo Testamento, que o povo judeu ouvia dos profetas que haveria , em um futuro, ou um fim de todos os males, em que Deus castigaria os injustos. já no Novo Testamento, Jesus mesmo faz menção deste tempo (kairoi). Ou seja, haveria um (eschaton-kairos)..
Diferenças na interpretação, tem se dividido entre os estudiosos. Isto se deu bem no tempo de Jesus Mt 16.1-4, de Paulo At 17.17,18. Segundo Bultmann, Jesus esteve de acordo com os escribas de seu tempo, em observar a lei. Mt 19.16-22.
Desde o primeiro século dominou-se entre os apóstolos o desejo de entender o antigo testamento. Contudo o método alegórico eram praticados por Clemente de Alexandria,Orígenes e Santo Agostinho.
Já na escola de Antioquia, havia grupos que tentaram evitar o letrismo e a alegoria que havia em Alexandria.
No seculo XII, surgiu Nicolau de Lyra que trouxe um significado importante do literal. Foi desta obra que Lutero se abrilhantou, e muitos creem que foi esta obra que influenciou-o a reforma (século XVI). Dai surgiu Calvino, e com ele surgiu grandes princípios para a interpretação moderna. Foi dentro desta época que surgiram várias escolas especializadas em "escatologia", cada uma com suas interpretações:
<strong><p>Pré-milenarismo</strong></p>
