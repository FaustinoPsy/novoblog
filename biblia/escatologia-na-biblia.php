<strong><p>Pos-tribulacao</strong></p>
Creem que Jesus arrebatará a sua igreja antes dos mil anos, e após o arrebatamento a terra passará pelo período da grande tribulação em que os judeus vão ser duramente perseguidos junto com os remanescentes que ficarem. Esta posição foi adotada por Agostinho, despopularizado em tempos futuros e revitalizado coma volta dos judeus a terra da palestina. Atualmente esta escola se tornou sinônimo do dispensacionalismo, mas há uma discordância entre os pré-milenaristas clássicos e os dispensacionalistas.
