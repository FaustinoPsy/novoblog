			<p><b>Semente da serpente , semente dupla ou linha dupla</b> é uma crença religiosa controversa que explica o relato bíblico da queda do homem ao dizer que a serpente se acasalou com Eva no Jardim do Éden , e o descendente de sua união foi Caim . Ele aparece nos primeiros escritos gnósticos , como o Evangelho de Filipe ( c. 350 ). Irineu ( c. 180 ), um pai da igreja primitiva, rejeitou explicitamente uma doutrina gnóstica semelhante como heresia , uma visão que foi ecoada por teólogos cristãos subseqüentes. [1] A doutrina da semente da serpente ocasionalmente tem sido promovida em tempos mais recentes, como pelos líderes religiosos americanos Daniel Parker (1781-1844), [2] William M. Branham (1909-1965), [3] : 98 e Arnold Murray (1929–2014). Essa crença é sustentada por alguns adeptos da teologia da supremacia branca conhecida como Identidade Cristã , que afirma que os judeus são descendentes da serpente. </p>
			<p>Valentinus (100-160) promoveu uma doutrina semelhante à semente da serpente que afirma que Eva se acasalou com a serpente (que era a encarnação de um Aeon chamado Sophia), mas não produziu descendência. Essa doutrina foi rejeitada pelos teólogos cristãos tradicionais, como Irineu. [1] A ideia de que Eva se acasalou com a serpente, ou Satanás , e produziu Caim, encontra sua expressão mais antiga nos escritos gnósticos (por exemplo, o Evangelho de Filipe). Em seu livro Cain: Filho da Serpente , David Max Eichhorn traça a idéia de volta ao início dos judeus Midrashic textos e identifica Muitos rabinos que ensinou que Caim era filho da união entre a serpente e Eva. [6]A ideia aparece em um livro do século 9 intitulado Pirke De-Rabbi Eliezer</p>
			
			<p><b>Daniel Parker </b></p>
Daniel Parker (1781-1844) foi um dos primeiros líderes americanos na Igreja Batista Primitiva no sul dos Estados Unidos e fundador de várias igrejas. Como um ancião , Parker liderou um grupo que se separou daquela igreja e formou os Batistas Predestinacionistas Duas Semente-no-Espírito . [7]

<p><b>William Branham </b></p>
<p>William M. Branham (1909-1965), um importante ministro americano nas décadas de 1940 e 1950, ensinou que Eva e a serpente tiveram relações sexuais e o nascimento de Caim foi o resultado disso.  Conseqüentemente, toda mulher carregava potencialmente a semente literal do diabo.  Os descendentes de Caim estavam hoje disfarçados de educados e cientistas,  que eram "um grande bando religioso de filhos bastardos ilegítimos". </p><p> Na versão de Branham da doutrina, a serpente não era o próprio Satanás, mas uma criatura subumana que ele descreveu como o "elo perdido" entre oschimpanzé e homem. 
De acordo com os ensinamentos de Branham, os pecados centrais da cultura moderna são o resultado da semente da serpente. A atitude de Branham em relação à cultura era uma perspectiva extremista que era semelhante ao conceito " Cristo contra a cultura ". </p>

<p><b>Arnold Murray </b></p>
<p>Arnold Murray (1929–2014), fundador da Capela do Pastor , ensinou a doutrina da semente da serpente. Ele aceitou a crença de que os judeus ( Reino de Judá ) descendiam de Adão até Sete , conforme descrito na Bíblia . No entanto, ele acreditava que os quenitas eram descendentes de Caim e também acreditava que eles haviam se infiltrado no reino do norte de Israel . [8] Os ensinamentos de Murray são contestados pelos ministérios de apologética protestantes CARM e pelo CRI . </p>

<p><b>Movimento Identidade Cristã </b></p>
<p>Os adeptos da teologia da supremacia branca, que é conhecida como Identidade Cristã de duas linhagens, sustentam a visão de que apenas os brancos são descendentes de Adão e, portanto, o povo escolhido de Deus . Os judeus são acreditados para ser os descendentes de Caim e, portanto, os descendentes de Satanás. Esta crença foi desenvolvida por Wesley A. Swift ( Igreja de Jesus Cristo - Cristão ), Conrad Gaard, Dan Gayman  ( Igreja de Israel ) e William Potter Gale entre outros. A facção oposta é chamada de Identidade Cristã de Linha Única e seus adeptos sustentam a visão de que todas as pessoas descendem de Adão, mas eles acreditam que apenas os arianos (ou seja, europeus do norte ) são verdadeiramente o povo escolhido de Deus. </p>

<b><p>P'ikareum </b></p>
<p>P'ikareum é um ritual que possivelmente é praticado em vários novos movimentos religiosos coreanos , no qual uma devota faz sexo com o líder masculino ( que afirma ser o messias ) para purificar seus descendentes do pecado.  O propósito deste ritual é desfazer o pecado original, que (como na doutrina clássica da semente da serpente) se acreditava ter sido cometido quando Eva fez sexo com a serpente.  O estudioso religioso britânico George Chryssides também observa que houve casos em que o líder messiânico era uma mulher e o neófito um homem. A pessoa assim iniciada terá relações sexuais com seu cônjuge, e a pureza que é adquirida do líder messiânico será transmitida tanto ao cônjuge quanto à progênie. O novo movimento religioso coreano mais notável que enfrentou acusações de realizar p'ikareum é a Igreja de Unificação de Sun Myung Moon ;  no entanto, Chryssides observa que, além do uso da linguagem que envolve a purificação de linhagens pecaminosas, não há evidência real de que este ritual ocorra dentro do movimento de Unificação.</p>
