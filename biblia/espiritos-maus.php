<p><b>De onde vêm os espíritos malignos?</b></p>
<hr>
</p><p>
<div><span style="font-size: medium;">Parece uma pergunta boba, não é? Pelo menos seria para a maioria dos crentes na Bíblia. Espíritos malignos vêm de Satanás. Mas não é isso que a Bíblia diz.</span></div>
<div><span style="font-size: medium;">A frase "espírito (s) maligno (s)" ocorre 14 vezes (em 13 versículos) na Bíblia, em 8 dos quais os espíritos malignos foram enviados direta e explicitamente por Deus.</span></div>
<div><span style="font-size: medium;"><strong>O primeiro espírito maligno foi enviado por Deus para matar pessoas em guerra.</strong></span></div>
<div><span style="font-size: medium;">Então Deus enviou um espírito maligno entre Abimeleque e os homens de Siquém; e os homens de Siquém negociaram traiçoeiramente com Abimeleque. Juízes 9:23</span></div>
<div><span style="font-size: medium;">Deus enviou o próximo espírito maligno para causar a Saul um colapso mental. (Mas, felizmente, Davi toca harpa e faz o "espírito maligno do Senhor" de Saul desaparecer.)</span></div>
<div><span style="font-size: medium;">Mas o Espírito do SENHOR se afastou de Saul, e um espírito maligno do SENHOR o perturbou. E os servos de Saul lhe disseram: Eis que agora um espírito maligno de Deus te perturba. Que nosso senhor ordene agora a teus servos, que estão diante de ti, que procurem um homem que é astuto numa harpa; e acontecerá, quando o espírito maligno de Deus estiver sobre ti, que ele deve tocar com ele. sua mão, e estarás bem. 1 Samuel 16: 14-16</span></div>
<div><span style="font-size: medium;">E aconteceu que, quando o espírito maligno de Deus estava sobre Saul, Davi tomou uma harpa e tocou com sua mão; então Saul se renovou e estava bem, e o espírito maligno se afastou dele. 1 Samuel 16:23</span></div>
<div><span style="font-size: medium;"><strong>Então Deus enviou um espírito maligno para fazer Saul profetizar e sentar com um dardo na mão.</strong></span></div>
<div><span style="font-size: medium;">No dia seguinte, veio o espírito maligno de Deus sobre Saul, e ele profetizou no meio da casa; e Davi brincou com a mão, como em outros momentos; e havia um dardo na mão de Saul. 1 Samuel 18:10</span></div>
<div><span style="font-size: medium;">E o espírito maligno do SENHOR estava sobre Saul, sentado em sua casa com o dardo na mão; e Davi brincava com a mão. 1 Samuel 19: 9</span></div>
<div><span style="font-size: medium;">As outras 6 ocorrências de "espírito maligno" estão no Novo Testamento. A Bíblia não diz de onde os espíritos malignos vieram, mas eles geralmente estão associados a algum tipo de doença (que vem de Deus, certo?).</span></div>
<div><span style="font-size: medium;">E naquela mesma hora ele curou muitas de suas enfermidades e pragas, e de espíritos malignos ; e para muitos cegos, ele deu vista. Lucas 7:21</span></div>
<div><span style="font-size: medium;">E certas mulheres, que haviam sido curadas de maus espíritos e enfermidades, Maria chamou Madalena, das quais foram sete demônios, Lucas 8: 2.</span></div>
<div><span style="font-size: medium;">De modo que do seu corpo foram trazidos lenços ou aventais doentes, e as doenças se afastaram deles, e os maus espíritos saíram deles. Então, alguns dos judeus vagabundos, exorcistas, convocaram-os a chamar aqueles que tinham espíritos malignos, o nome do Senhor Jesus, dizendo: Adjudicamo-lo por Jesus a quem Paulo prega. ... E o espírito maligno respondeu e disse: Jesus eu sei, e Paulo eu sei; mas quem sois? E o homem em quem o espírito maligno se lançou sobre eles, venceu-os e prevaleceu contra eles, de modo que fugiram daquela casa nus e feridos. Atos 19: 12-16</span></div>
<div><span style="font-size: medium;">Portanto, a Bíblia é bastante clara sobre os espíritos malignos: <strong>eles são enviados diretamente por Deus</strong> ou sua origem é desconhecida. A Bíblia nunca atribui espíritos malignos a Satanás.</span></div>
