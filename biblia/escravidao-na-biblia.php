<h2 style="margin: 1em 0px 0.25em; padding: 0px; overflow: hidden; border-bottom: 1px solid #a2a9b1; font-weight: normal; font-family: &quot;Linux Libertine&quot;, Georgia, Times, serif; line-height: 1.3;"><span id="Composition" class="mw-headline">Escravidão na bíblia </span></h2>
<span style="font-size: medium;">Exceto pelo assassinato, a escravidão deve ser uma das coisas mais imorais que uma pessoa pode fazer. No entanto, a escravidão é desenfreada em toda a Bíblia, tanto no Antigo quanto no Novo Testamento. A Bíblia aprova claramente a escravidão em muitas passagens, e chega ao ponto de dizer como obter escravos, quão duro você pode vencê-los e quando você pode fazer sexo com escravas.</span></div>
<div><span style="font-size: medium;">Muitos judeus e cristãos tentarão ignorar os problemas morais da escravidão dizendo que esses escravos eram na verdade servos ou servos contratados. Muitas traduções da Bíblia usam a palavra "servo", "servo" ou "servo" em vez de "escravo" para fazer a Bíblia parecer menos imoral do que realmente é. Embora muitos escravos possam ter trabalhado como empregados domésticos, isso não significa que não eram escravos que foram comprados, vendidos e tratados pior que o gado.</span></div>
<div><span style="font-size: medium;">A passagem a seguir mostra que os escravos são claramente propriedades para serem comprados e vendidos como gado.</span></div>
<div><span style="font-size: medium;"><br /></span></div>
<div><span style="font-size: medium;">No entanto, você pode comprar escravos masculinos ou femininos dentre os estrangeiros que vivem entre vocês. Você também pode comprar os filhos desses estrangeiros residentes, incluindo aqueles que nasceram em sua terra. Você pode tratá-los como sua propriedade, transmitindo-os aos seus filhos como uma herança permanente. Você pode tratar seus escravos assim, mas o povo de Israel, seus parentes, nunca devem ser tratados dessa maneira. (Levítico 25: 44-46 NLT)</span></div>
<div><span style="font-size: medium;"><br /></span></div>
<div><span style="font-size: medium;">A passagem a seguir descreve como os escravos hebreus devem ser tratados.</span></div>
<div><span style="font-size: medium;">Se você compra um escravo hebreu, ele deve servir por apenas seis anos. Liberte-o no sétimo ano, e ele não lhe deve nada pela liberdade dele. Se ele estava solteiro quando se tornou seu escravo e depois se casou depois, somente ele será libertado no sétimo ano. Mas se ele era casado antes de se tornar escravo, sua esposa será libertada com ele. Se seu mestre lhe deu uma esposa enquanto ele era escravo, e eles tiveram filhos ou filhas, então o homem estará livre no sétimo ano, mas sua esposa e filhos ainda pertencerão a seu mestre. Mas o escravo pode declarar claramente: 'Amo meu senhor, minha esposa e meus filhos. Prefiro não me libertar. Se ele fizer isso, seu mestre deve apresentá-lo diante de Deus. Então seu mestre deve levá-lo até a porta e furar publicamente seu ouvido com um furador. Depois disso, o escravo pertencerá ao seu mestre para sempre. (Êxodo 21: 2-6 NLT)</span></div>
<div><span style="font-size: medium;"><br /></span></div>
<div><span style="font-size: medium;">Observe como eles podem fazer com que um escravo hebreu se torne um escravo permanente mantendo sua esposa e filhos reféns até que ele diga que quer se tornar um escravo permanente. Que tipo de valores familiares são esses?</span></div>
<div><span style="font-size: medium;">A passagem a seguir descreve a prática doentia da escravidão sexual. Como alguém pode pensar que é moral vender sua própria filha como escrava sexual?</span></div>
<div><span style="font-size: medium;">Quando um homem vende sua filha como escrava, ela não será libertada ao fim de seis anos como os homens. Se ela não agradar o homem que a comprou, ele pode permitir que ela seja comprada novamente. Mas ele não tem permissão para vendê-la a estrangeiros, pois foi ele quem rompeu o contrato com ela. E se o dono da escrava providenciar que ela se case com seu filho, ele não poderá mais tratá-la como escrava, mas deverá tratá-la como sua filha. Se ele próprio se casa com ela e depois toma outra esposa, ele não pode reduzir a comida ou a roupa dela ou deixar de dormir com ela como esposa. Se ele falhar em qualquer uma dessas três maneiras, ela poderá sair como uma mulher livre sem fazer nenhum pagamento. (Êxodo 21: 7-11 NLT)</span></div>
<div><span style="font-size: medium;"><br /></span></div>
<div><span style="font-size: medium;">Então, esses são os valores da família da Bíblia! Um homem pode comprar quantos escravos sexuais quiser, desde que os alimente, os vista e faça sexo com eles!</span></div>
<div><span style="font-size: medium;">O que a Bíblia diz sobre espancar escravos? Ele diz que você pode derrotar escravos homens e mulheres com uma vara com tanta força que, desde que eles não morram imediatamente, você estará livre de qualquer ação errada</span></div>
<div><span style="font-size: medium;">Quando um homem golpeia seu escravo com uma vara com tanta força que o escravo morre sob sua mão, ele será punido. Se, no entanto, o escravo sobreviver por um dia ou dois, ele não será punido, uma vez que o escravo é de sua propriedade. (Êxodo 21: 20-21 NAB)</span></div>
<div><span style="font-size: medium;">Você pensaria que Jesus e o Novo Testamento teriam uma visão diferente da escravidão, mas a escravidão ainda é aprovada no Novo Testamento, como mostram as seguintes passagens.</span></div>
<div><span style="font-size: medium;">Escravos, obedeçam a seus senhores terrenos com profundo respeito e medo. Sirva-os sinceramente como você serviria a Cristo. (Efésios 6: 5 NLT)</span></div>
<div><span style="font-size: medium;">Os cristãos escravos devem respeitar plenamente seus senhores, para que o nome de Deus e seus ensinamentos não sejam envergonhados. Se seu mestre é cristão, isso não é desculpa para ser desrespeitoso. Você deve trabalhar ainda mais porque está ajudando outro crente por seus esforços. Ensine essas verdades, Timóteo, e incentive todos a obedecê-las. (1 Timóteo 6: 1-2 NLT)</span></div>
<div><span style="font-size: medium;"><br /></span></div>
<div><span style="font-size: medium;">Na parábola a seguir, Jesus claramente aprova os espancamentos de escravos, mesmo que eles não soubessem que estavam fazendo algo errado.</span></div>
<div><span style="font-size: medium;">O servo será severamente punido, pois, embora conhecesse seu dever, recusou-se a cumpri-lo. “Mas as pessoas que não sabem que estão fazendo algo errado serão punidas apenas levemente. Muito é exigido daqueles a quem muito é dado, e muito mais é exigido daqueles a quem muito mais é dado. ” (Lucas 12: 47-48 NLT)</span></div>
<div><span style="font-size: medium;">Isso sem falar que paulo não ordena que Filemon liberte seu escravo figitivo, mas que o reeba novamente...</span>
